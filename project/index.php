<div class="use">
    <h5>اين تستخدم لغه البايثون</h5>
    <p>تُستخدم لغة بايثون في كل المجالات، فهي لغة برمجة متعددة الأغراض، ومن مجالات استخدامها: تحليل البيانات، والروبوتات، وتعلم
    الآلة، وتطبيقات REST، وتطوير المواقع والألعاب، والرسوم ثلاثية الأبعاد، والأتمتة وبرمجة الأنظمة المدمجة، والكثير من
    المجالات الأخرى التي لا يسعنا حصرها هنا.
    
    تستخدم الكثير من المواقع والشركات العملاقة لغة بايثون، ومنها Spotify و Google وAmazon، إضافة إلى Facebook التي تستخدم
    بايثون لمعالجة الصور. وفي كل يوم تتحول شركات جديدة إلى استخدام بايثون، مثل Instagram التي قررت مؤخرًا استخدامها وفضلتها
    على PHP. تُستخدم بايثون أيضًا من قبل بعض الجهات العلمية والبحثية، مثل وكالة الفضاء الأمريكية ناسا، والتي لها</p>
