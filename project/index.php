<div class="lap">
    <h2>مكتبات بايثون</h2>
    <br>
<h4>مكتبة Requests</h4>

إذا كنت ترغب بإنشاء اتصال عبر بروتوكول HTTP، فإن هذه المكتبة هي خيارك المناسب للقيام بذلك وبشكل أتوماتيكي دون الحاجة إلى
الجهد اليدوي في ذلك وبالتالي يتم استخدام هذه المكتبة عند طلب صفحة HTML، فهي تقوم بمعرفة كل ما يقوم به المتصفح في الخلفية
وبالتالي تعطيك المعلومات والتوثيق الكامل للاتصال والقوة الكاملة للتعامل مع أي استعلام يعتمد على برتوكول HTTP. فهي قوية
وسهلة الاستخدام وكل ذلك عن طريق urllib3 (المكتبة الأم للمكتبة request).
<br>
<br>

<h4>المكتبة Pluggy</h4>
تعتبر من المكتبات الجديدة نسبياً في لغة بايثون وتعطيك المرونة اللازمة للتعامل مع الملحقات (الإضافات) من إضافة وتعديل
وإدارة، حيث تمكّنك من التعامل مع أكثر من 500 إضافة (أو ما يسمى Plugin) وبالتالي فهي تعطيك قوة كبيرة جداً لتطبيقك البرمجي
بحيث تمكّنك من التعامل مع البرنامج المضيف وكأنه جزء من الكود البرمجي الخاص بك.
<br>
<br>
<h4>مكتبة Scappy</h4>
تعتبر Scappy أداة قوية ومهمة لمعالجة بيانات الحزم المرسلة، حيث تمكّنك من التقاط تلك الحزم وبالتالي تحليلها والتعرف على
محتواها، وهي قادرة على القيام بالعديد من المهام المتعلقة باكتشاف الشبكة وعمليات التتبع وإرسال الإطارات الخاطئة والحقن
لغايات الاختراق والأمان، وكذلك الدمج بين مجموعة من التقنيات (مثل VLAN مع ARP وغيرها) و لعل أكثر من يهتم بهذه المكتبة هم
مستخدمو نظام التشغيل لينوكس.
<br>
<br>
<h4>مكتبة Beautiful Soup</h4>
تستخدم هذه المكتبة بشكل رئيسي لاستخراج المعلومات من صفحة HTML كما تفيد في تحويل البيانات غير المنسقة (غير المهيكلة) إلى
بيانات منسقة بترميز UTF-8 (وهو الترميز القياسي العالمي للمحارف)، ليس هذا فحسب بل إنها أيضاً تعطيك القوة والإمكانية
للتحويل بين أنواع مختلفة من ملفات البيانات (مثل ملفات XML) فهي قادرة على التعامل مع أنواع مهمة جداً من البيانات وخاصة
XML (وكذلك ملفات HTML) بسهولة وبالتالي فهي توفر على المبرمج الكثير من الوقت والجهد.
<br>
<br>
<h4>مكتبة SQLAlchemy</h4>
تعطي هذه المكتبة مبرمجي بايثون القوة والمرونة الكاملة للتعامل مع لغة الاستعلامات المهيكلة SQL، وتمكنك هذه المكتبة من
إجراء العمليات الرئيسية في التعامل مع قواعد البيانات من نوع SQL من عمليات الوصول والتعديل وغيرها فهي تنظر إلى قاعدة
البيانات ليس فقط كمجموعة من الجداول بل أيضاً كمجموعة من العلاقات التي يمكنك عبرها تجميع البيانات والحصول عليها من
الجداول.
<br>
<br>
<h4>مكتبة Matplotlib</h4>
إذا كنت كمبرمج بايثون بحاجة إلى الطباعة ثنائية البعد أي تريد رسم مخططات معينة سواء أكانت مخططات رياضية أو مخططات إحصائية
(كمخططات barchart ومخططات Pies)، فأنت بحاجة إلى هذه المكتبة الرائعة لتضمنها ضمن الكود البرمجي الخاص بك، ليس هذا فحسب بل
إنها تقدم لك أيضاً القدرة على إظهار مخططات صورية وخطية وكذلك مخططات ملونة ومجموعة كبيرة من المخططات الأخرى
<br>
<br>
<h4>مكتبة pygame</h4>
من منا لا يحب أن يبرمج لعبة أو خطر بباله ذلك يوماً ما؟ أعتقد أن الأغلبية تحلم بذلك، فإن كنت مبرمج بايثون يمكنك ببساطة
تصميم الألعاب ثنائية البعد بالاعتماد على هذه المكتبة الرائعة، والتي تعتبر من المكتبات الشهيرة والقوية في تطوير الألعاب
ثنائية البعد في بيئة البايثون. وهي مفتوحة المصدر ومجانية أيضاً وما تقدمه لك هذه المكتبة ليس فقط تطوير الألعاب بل تساعدك
في أي تطبيق وسائط متعددة تريد تطويره باستخدام بايثون. 
<br>
<br>
<h4>مكتبة pyGtk</h4>
إن كنت تبحث عن تصميم تطبيقات بواجهات المستخدم الرسومية (GUI) ونقصد بواجهات المستخدم الرسومية الإطارات والأزرار والحقول
وغيرها من العناصر المرئية، فإن هذه المكتبة أحد الخيارات المهمة المتاحة أمامك والتي تقدم لك الإمكانيات الكاملة لبناء
تطبيقات الواجهات الرسومية، ومن أشهر البرامج التي تم بناؤها بالاعتماد عليها تطبيق BitTorrent الشهير فلا تتردد بالحصول
عليها واستخدامها في تطبيقاتك.

