    <h3>تاريخ البايثون</h3>
    <p>ظهرت لغة بايثون في أواخر الثمانينيات على يد غيدو فان روسوم (Guido van Rossum)، وقد عُدَّت خليفةً للغة ABC). كما استفادت
    من الكثير من اللغات السابقة لها، مثل Modula-3 و C و C++‎ و Algol-68 و SmallTalk، وغيرها من اللغات.
    
    نُشِر الإصدار 2.0 من لغة بايثون عام 2000، وقد قدّم العديد من الميزات الجديدة، مثل القوائم الفهمية (List Comprehensions)،
    ونظام كنس المُهملات (garbage collection). وفي عام 2008، ظهر الإصدار بايثون 3.0، والذي شكّل طفرة في اللغة، بيْد أنه لم
    يكن متوافقًا تمامًا مع الإصدارات السابقة، لذلك قرر فريق التطوير الاستمرار في دعم إصدار أخير من سلسلة بايثون ‎2.x‎، وهو
    بايثون 2.7 حتى عام 2020.</p>
