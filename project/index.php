    <h2>البايثون</h2>
    <p>هي لغة برمجة، عالية المستوى سهلة التعلم مفتوحة المصدر قابلة للتوسيع، تعتمد أسلوب البرمجة الكائنية (OOP). لغة بايثون هي
    لغة مُفسَّرة، ومُتعدِدة الاستخدامات، وتستخدم بشكل واسع في العديد من المجالات، كبناء البرامج المستقلة باستخدام الواجهات
    الرسومية وفي تطبيقات الويب، ويمكن استخدامها كلغة برمجة نصية للتحكم في أداء العديد من البرمجيات مثل بلندر. بشكل عام، يمكن
    استخدام بايثون لعمل البرامج البسيطة للمبتدئين، ولإنجاز المشاريع الضخمة في الوقت نفسه. غالباً ما يُنصح المبتدؤون في ميدان
    البرمجة بتعلم هذه اللغة لأنها من بين أسرع اللغات البرمجية تعلماً. طُوِّرت بايثون في معهد الرياضيات والمعلوماتية الهولندي
    (CWI) في مدينة أمستردام على يد جايدو فان روسم في أواخر ثمانينات القرن العشرين، وكان أول إعلان عنها في عام 1991م. كُتبت
    نواة اللغة باستعمال لغة سي. أطلق روسم الاسم "بايثون" على لغته تعبيرًا عن إعجابه بفِرقَة مسرحية هزلية شهيرة من بريطانيا،
    كانت تطلق على نفسها اسم مونتي بايثون. تتميز بايثون بمجتمعها النشط، كما أن لها الكثير من المكتبات البرمجية ذات الأغراض
    الخاصة التي برمجها أشخاص من ذلك المجتمع. مثلاً، هناك مكتبة باي جايم التي توفر مجموعة من الدوال من أجل برمجة الألعاب.
    يمكن لبايثون أيضاً التعامل مع العديد من أنواع قواعد البيانات مثل ماي إس كيو إل وغير ذلك. تدعم بايثون أنماط برمجية متعددة
    هي التوجيه الكائني، البرمجية جانبية التوجيه والبرمجة الوظيفية. تُستخدم بايثون عادةً مثل العديد من لغات البرمجة
    الديناميكية كلغة برمجة نصية. بايثون لديها نموذج مفتوح للتطوير، قائم على مجتمع بايثون البرمجي ومدعوم من مؤسسة برمجيات
    بايثون. والتي تحافظ على تعريف اللغة في التنفيذ المرجعي لسي بايثون.</p>
